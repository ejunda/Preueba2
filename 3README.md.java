    private void txtCedulaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCedulaFocusLost
        // TODO add your handling code here:
        String codigo;
        int multiplo=2;
        int cont=0;

        for (int x=0;x<txtCedula.getText().length();x++){
            cont=cont+(Integer.parseInt(txtCedula.getText().substring(txtCedula.getText().length()-x-1, txtCedula.getText().length()-x))*multiplo);
            multiplo++;
            if(multiplo==8){
                multiplo=2;
            }
        }
        cont=11-(cont%11);

        if(cont<=9){
            codigo=""+cont;
        }else if(cont==11){
            codigo="0";
        }else{
            codigo="k";
        }
        if(codigo!=null){
            txtValidador.setText(codigo);
        }


        if(txtCedula.getText().length()>=7){
            JLImagenCedula.setIcon(icono);
        }else{
            JLImagenCedula.setIcon(null);
        }
    }//GEN-LAST:event_txtCedulaFocusLost
