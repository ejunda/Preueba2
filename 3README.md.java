    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        // TODO add your handling code here:
       if(txtNombre.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese el nombre");
           return;
       }
           if(txtApellidos.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese el apellidos");
           return;
       }
               if(txtCedula.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese el su cedula");
           return;
       }
                   if(txtValidador.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese el Código Validador");
           return;
       }
           if(txtEmail.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese su email");
           return;
       }
           if(txtTelefono.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor ingrese su telefono");
           return;
       }
    String Nombre, Apellidos, Cedula, Email, Telefono;

    Nombre=txtNombre.getText();
    Apellidos=txtApellidos.getText();
    Cedula=txtCedula.getText()+"-"+txtValidador.getText();
    Email=txtEmail.getText();
    Telefono=txtTelefono.getText();


    Persona persona=new Persona(Nombre,Apellidos,Cedula,Email,Telefono);
    agenda.GuardarPersona(persona);

    JOptionPane.showMessageDialog(null, " Nombre: "+Nombre+" Apellido: "+Apellidos+" Cédula: "+Cedula+" Email: "+Email+" Teléfono: "+Telefono+"");

    }//GEN-LAST:event_BotonGuardarActionPerformed

